# Operadores aritmeticos
numero1 = 80
numero2 = 4 # operador de asignación =

resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2
resto = numero1 % numero2 # El operador % divide pero muesta el resto de la división

print("**********CALCULADORA***********")
# También se puede realizar directamente la operación sin necesidad de meterla a una variable
print(f"La suma es: {numero1 + numero2}")
print(f"La resta es: {resta}")
print(f"La resta es: {multiplicacion}")
print(f"La resta es: {division}")
print(f"El residuo de la división es: {resto}")